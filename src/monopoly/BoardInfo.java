package monopoly;

/**
 *
 * @author Kyle
 */
public class BoardInfo {
    String name;
    int id;
            
    public BoardInfo() {
        this.name = "";
        this.id = 0;
    }
    
    public BoardInfo(String name, int id) {
        this.name = name;
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
    
    @Override
    public String toString() {
        return this.name;
    }
}
